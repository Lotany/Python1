check_season = input("Enter month")
if check_season == 'september' or 'october' or 'november':
    print("The season is Autum")
elif check_season == 'december' or 'january' or 'february':
    print("The season is winter")
elif check_season == 'june' or 'july' or 'august':
    print("the season is summer")
else:
    print("Out of season")

